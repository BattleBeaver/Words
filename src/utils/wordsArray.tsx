const getSelectedCountFromArray = (arr: Array<string>, count: number) => {
  arr.sort(() => Math.random() - 0.5);
  return arr.slice(0, count);
};

const getWordsArray = (count: number, lang: string): string[] => {
  let result: string[] = [];
  switch (lang) {
    case "en":
      result = getSelectedCountFromArray(enKobzar, count);
      break;
    case "ua":
      result = getSelectedCountFromArray(uaKobzar, count);
      break;
  }
  return result;
};

export default getWordsArray;
